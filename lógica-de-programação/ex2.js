// operadores relacionais: <     >      >=      <=      !=     ==

// < menor que
// > maior que
// >= maior ou igual a
// <= menor ou igual a
// != diferente de
// === igual a 


let valor = 100;

if (valor>1000){
    console.log("O valor é maior que 1000"+ " " + valor)
}

else if (valor === 1000){
    console.log("O valor é igual a 1000" + " " + valor)
}

else{
    console.log("O valor é menor que 1000"+ " " + valor)
}